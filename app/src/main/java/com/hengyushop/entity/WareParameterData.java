package com.hengyushop.entity;

public class WareParameterData {

	public int wareid;
	public String specParameterName;
	public String specParameterValue;

	public int getWareid() {
		return wareid;
	}

	public void setWareid(int wareid) {
		this.wareid = wareid;
	}

	public String getSpecParameterName() {
		return specParameterName;
	}

	public void setSpecParameterName(String specParameterName) {
		this.specParameterName = specParameterName;
	}

	public String getSpecParameterValue() {
		return specParameterValue;
	}

	public void setSpecParameterValue(String specParameterValue) {
		this.specParameterValue = specParameterValue;
	}

}
